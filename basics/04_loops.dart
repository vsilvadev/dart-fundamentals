void main() {
  //FOR: will exececute a block of code a specific number of times.
  for (int i = 0; i <= 10; i++) {
    print('For print: $i'); //wil print 0 to 10
  }

  //WHILE: will execute a block of code while a condition is true.
  int counter = 10;
  while (counter != 1) {
    counter = counter - 1;
    print('While counter: $counter'); //wil print 0 to 10
  }
}
